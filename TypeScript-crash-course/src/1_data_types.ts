// number
let myNum: number;
myNum = 3.14;

// string
let myStr: string;
myStr = 'Hello, world!';

// boolean
let myBool: boolean;
myBool = true;

// array
let strArr: string[];
strArr = ['Hello', 'World'];

// null
let myNull: null;
myNull = null;

// undefined
let myUndefined: undefined;
myUndefined = undefined;

// tuple
let numStrTuple: [number, string];
numStrTuple = [3.14, 'Hello'];

// Data type combination
let age: number | string;
age = 26;
age = '26'; // Also works
